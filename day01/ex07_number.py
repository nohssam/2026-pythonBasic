# 연산자
# 산술 연산자 (+,-,*,/, //(몫), %(나머지), **(거듭제곱)
# 복합 연산자 (+=, -=, *=, /=, //=, %=, **= )

su1 = 7
su2 = 3

res = su1 + su2
print(f"{res=}")

res = su1 - su2
print(f"{res=}")

res = su1 * su2
print(f"{res=}")

res = su1 / su2
print(f"{res=}")

res = su1 // su2
print(f"{res=}")

res = su1 % su2
print(f"{res=}")

res = su1 ** 3  # 343 = 7 * 7 * 7
print(f"{res=}")
print()

su2 += 7
print(f"{su2=}")

su2 -= 3
print(f"{su2=}")

